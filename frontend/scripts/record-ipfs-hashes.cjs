const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

const results = {
  mechs: {},
  animations: {},
  gateway: 'https://gateway.pinata.cloud/ipfs/'
};

console.log('🚀 MechForge IPFS Upload Helper\n');
console.log('This tool helps you record IPFS hashes after manual upload.\n');
console.log('Instructions:');
console.log('1. Go to https://app.pinata.cloud/pinmanager');
console.log('2. Upload each file using the web interface');
console.log('3. Copy the IPFS hash (starts with Qm...)');
console.log('4. Paste it here when prompted\n');

const mechsDir = path.join(__dirname, '../public/mechs');
const mechFiles = fs.readdirSync(mechsDir).filter(f => f.endsWith('.glb'));

async function promptForHash(filename, type = 'mech') {
  return new Promise((resolve) => {
    rl.question(`Enter IPFS hash for ${type} "${filename}": `, (hash) => {
      const trimmedHash = hash.trim();
      if (trimmedHash.startsWith('Qm') && trimmedHash.length === 46) {
        resolve(trimmedHash);
      } else if (trimmedHash === '') {
        console.log('  Skipping (empty)');
        resolve('');
      } else {
        console.log('  Invalid hash format. Should start with Qm and be 46 chars.');
        resolve(promptForHash(filename, type));
      }
    });
  });
}

async function processMechs() {
  console.log('\n📁 MECH MODELS:\n');
  
  for (const file of mechFiles) {
    const hash = await promptForHash(file, 'mech');
    if (hash) {
      results.mechs[file] = {
        hash,
        url: `https://gateway.pinata.cloud/ipfs/${hash}`
      };
      console.log(`  ✅ Recorded: ${hash}\n`);
    }
  }
}

async function processAnimations() {
  const animationsDir = path.join(__dirname, '../public/animations');
  const animFolders = fs.readdirSync(animationsDir).filter(f => 
    fs.statSync(path.join(animationsDir, f)).isDirectory()
  );
  
  console.log('\n📁 ANIMATIONS:\n');
  
  for (const folder of animFolders) {
    const folderPath = path.join(animationsDir, folder);
    const animFiles = fs.readdirSync(folderPath).filter(f => f.endsWith('.glb'));
    
    console.log(`Folder: ${folder}/`);
    results.animations[folder] = {};
    
    for (const file of animFiles) {
      const hash = await promptForHash(`${folder}/${file}`, 'animation');
      if (hash) {
        results.animations[folder][file] = {
          hash,
          url: `https://gateway.pinata.cloud/ipfs/${hash}`
        };
        console.log(`  ✅ Recorded: ${hash}\n`);
      }
    }
  }
}

function generateConfig() {
  const mechMapping = {
    'omega_textured.glb': 1,
    'vanguard_textured.glb': 2,
    'scout_textured.glb': 3,
    'striker_textured.glb': 4
  };
  
  const config = {
    gateway: results.gateway,
    mechs: {},
    animations: {}
  };
  
  // Build mech config
  for (const [file, data] of Object.entries(results.mechs)) {
    const type = mechMapping[file];
    if (type) {
      config.mechs[type] = data.hash;
      if (file === 'striker_textured.glb') {
        config.mechs[5] = data.hash; // Support uses striker
      }
    }
  }
  
  // Build animation config
  for (const [folder, files] of Object.entries(results.animations)) {
    config.animations[folder] = {};
    for (const [file, data] of Object.entries(files)) {
      const animType = file.replace(`${folder}_`, '').replace('.glb', '');
      config.animations[folder][animType] = data.hash;
    }
  }
  
  return config;
}

function writeConfigFiles(config) {
  // Save JSON
  const jsonPath = path.join(__dirname, 'ipfs-config.json');
  fs.writeFileSync(jsonPath, JSON.stringify(config, null, 2));
  
  // Generate JS file
  const jsContent = `// IPFS Configuration for MechForge 3D Assets
// Auto-generated by record-ipfs-hashes.js

export const IPFS_GATEWAY = '${config.gateway}';

export const MECH_MODELS_IPFS = {
  1: '${config.mechs[1] || ''}', // Assault - omega_textured.glb
  2: '${config.mechs[2] || ''}', // Tank - vanguard_textured.glb
  3: '${config.mechs[3] || ''}', // Scout - scout_textured.glb
  4: '${config.mechs[4] || ''}', // Sniper - striker_textured.glb
  5: '${config.mechs[5] || ''}', // Support - striker_textured.glb
};

export const MECH_ANIMATIONS_IPFS = {
${Object.entries(config.animations).map(([folder, anims]) => {
  const animEntries = Object.entries(anims).map(([name, hash]) => `    ${name}: '${hash}'`).join(',\n');
  return `  ${folder}: {\n${animEntries}\n  }`;
}).join(',\n')}
};

// Helper function to get the best available URL for a mech model
export function getMechModelUrl(mechType, preferIPFS = true) {
  const ipfsHash = MECH_MODELS_IPFS[mechType];
  
  if (preferIPFS && ipfsHash) {
    return \`\${IPFS_GATEWAY}\${ipfsHash}\`;
  }
  
  // Fallback to local paths
  const localPaths = {
    1: '/mechs/omega_textured.glb',
    2: '/mechs/vanguard_textured.glb',
    3: '/mechs/scout_textured.glb',
    4: '/mechs/striker_textured.glb',
    5: '/mechs/striker_textured.glb',
  };
  
  return localPaths[mechType] || localPaths[1];
}
`;
  
  const jsPath = path.join(__dirname, '../src/config/ipfs.js');
  fs.writeFileSync(jsPath, jsContent);
  
  return { jsonPath, jsPath };
}

async function main() {
  try {
    await processMechs();
    await processAnimations();
    
    console.log('\n📊 Generating configuration files...\n');
    
    const config = generateConfig();
    const { jsonPath, jsPath } = writeConfigFiles(config);
    
    console.log('✅ Configuration saved!');
    console.log(`   JSON: ${jsonPath}`);
    console.log(`   JS:   ${jsPath}\n`);
    
    console.log('📋 Summary:');
    console.log(`   Mechs: ${Object.keys(config.mechs).length} types`);
    console.log(`   Animations: ${Object.keys(config.animations).length} folders\n`);
    
    console.log('📝 Next steps:');
    console.log('   1. Open frontend/src/components/Mech3DViewer.jsx');
    console.log('   2. Set USE_IPFS = true');
    console.log('   3. Rebuild and deploy\n');
    
  } catch (error) {
    console.error('❌ Error:', error.message);
  } finally {
    rl.close();
  }
}

main();
